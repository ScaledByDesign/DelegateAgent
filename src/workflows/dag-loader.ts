/**
 * Phase 1 — Archon DAG loader with Hephaestus back-compat shim.
 *
 * Dispatches between two on-disk shapes per workflow directory:
 *
 *   1. Single-file DAG workflow — `workflows/<name>/workflow.yaml` has a
 *      top-level `nodes:` array. Validated against `workflowDefinitionSchema`
 *      (the ported Archon shape) and returned as a `DagWorkflowWithSource`.
 *
 *   2. Hephaestus split workflow — `workflows/<name>/workflow.yaml` has
 *      `phase_order:` but NO `nodes:`, plus a `phases/NN_<id>.yaml` file per
 *      phase. Loaded via the EXISTING `validateWorkflowConfig` / `validatePhase`
 *      from `./types.ts`, then adapted into a synthetic `WorkflowDefinition`
 *      whose nodes are a linear chain of `prompt:` nodes (one per phase, each
 *      depending on the previous). This preserves the 5 existing Hephaestus
 *      workflows (`bug-fix`, `doc-gen`, `feature-dev`, `index-repo`,
 *      `prd-to-software`) unchanged on disk while letting the new executor
 *      (Phase 2) treat them uniformly.
 *
 * Secret-pattern reject (per plan Phase 1 + acceptance gate): for every
 * `bash:` or `script:` body in a DAG workflow, scan for `$<NAME>_API_KEY`,
 * `$<NAME>_SECRET`, `$<NAME>_TOKEN` references and reject at load time so
 * authors can't accidentally smuggle secrets through `process.env` expansion.
 * Allowlist of env names that bash/script nodes can reference is in the
 * executor's `env-sanitizer.ts` (Phase 2); the load-time check is a
 * defense-in-depth complement.
 */

